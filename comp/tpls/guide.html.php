        <div class="title">中国银行间市场交易商协会单位会员入会指南（修订稿）</div>
        <div class="content">
        为方便广大市场成员加入协会，根据《中国银行间市场交易商协会章程》及相关规定制定本指南。<br />
        一、入会条件<br />
        （一）自愿加入协会，接受协会自律管理；<br />
        （二）拥护《中国银行间市场交易商协会章程》；<br />
        （三）中华人民共和国境内法人和其他相关机构；<br />
        （四）从事银行间市场相关业务；<br />
        （五）协会要求的其他条件。<br />
        其中，发行债务融资工具的非金融企业入会除上述需具备的五项条件外，还需具备下列两项条件：<br />
        （一）生产经营符合国家宏观调控政策、产业政策、环保政策及相关法律法规；<br />
        （二）近三年未发生重大违法违规事件以及未受国家行政机关的行政处罚。<br />
        二、申请成为会员须提交的材料<br />
        （一）入会申请书（分支机构还须许可或授权书面文件）；<br />
        （二）本单位章程；<br />
        （三）营业执照副本或执业许可证副本复印件；<br />
        （四）组织架构简介和主要负责人简历；<br />
        （五）从事银行间市场相关业务情况介绍；<br />
        包括但不限于以下内容：一级市场业务、二级市场业务、衍生产品业务、取得哪些业务资格及业务开展情况介绍、创新业务开展情况、研究分析能力介绍；<br />
        （六）具体业务规章制度、业务流程、风控制度；<br />
        （七）关于无违法和重大违规行为的说明；<br />
        （八）最近一年的审计报告;<br />
        （九）会员登记信息备案表。<br />
        其中，第（一）、（五）、（七）、（九）项材料可注册后登录会员信息管理系统（http://huiyuan.nafmii.org.cn），参考相应模板进行填写。<br />
        三、入会程序<br />
        （一）申请单位准备好申请材料后，需注册并登录会员信息管理系（http://huiyuan.nafmii.org.cn），递交电子版材料，协会工作人员对其申请材料进行初查并在系统中予以反馈，确认其是否符合协会要求；<br />
        （二）经协会工作人员确认和通知，申请单位将纸质版申请材料加盖公章及骑缝章，快递至协会秘书处，协会工作人员对申请材料进行复查；
